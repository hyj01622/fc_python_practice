# 숫자 자료형
# 1. 정수형 : 소수점이 없는 수
print(1, 5, -1, 0)

# 2. 실수형 : 소수점이 있는 수
print(.1, 0, -1.4)

# 문자열 자료형
# "" or ''
print("hello python")
print('hello python')
print('"개 짖는 소리 좀 안나게 해라" 라고 그는 외쳤다.')

# 불리언 자료형
# True or False
print(True)
print(False)

# 공백 없애고 싶을때
# sep=""
print(1, 2, 3, sep="")

# 문자 한줄로 나타내고 싶을때
# end=""
print(1, 2, 3, sep="", end="")
print(1, 2, 3, sep="")