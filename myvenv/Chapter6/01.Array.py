# 배열 a = [1, 2, 3]
# 배열 b = [3, 4, 2, 1]

# 추가 : a.append(4) // [1, 2, 3, 4]
# 할당 : a[0] = 5 // [5, 2, 3, 4]
# 삭제 : del a[0] // [2, 3, 4]

# 슬라이싱 : b[1: 3] // [4, 2] (리스트[시작 : 끝 + 1])
# 길이 : len(b) // 4
# 정렬 : b.sort() // [1, 2, 3, 4]

# 마지막 아이템 : a[-1] // 3

# 슬라이싱에서 : 만 주면 전체 다 가져오기
# 슬라이싱에서 시작인덱스 생략하면 자동으로 0부터 시작
# 슬라이싱에서 끝 인덱스 생략하면 시작 인덱스부터 끝까지

# 정렬에서 내림차순 하고싶을땐 reverse=True를 인자로 넘기면 됨
